
package Day;

public class PostPreIncrementDecrement {
    public static void main(String[] args) {
        int x=10;
        int y;
         y=x++;
         System.out.println(""+y);
         y=x--;
         System.out.println(""+y);
         y=x--;
         System.out.println(""+y);
         y=x++;
         System.out.println(""+y);
         
    }
    
}
