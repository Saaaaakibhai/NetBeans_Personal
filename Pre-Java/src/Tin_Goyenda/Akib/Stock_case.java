package Tin_Goyenda.Akib;

public class Stock_case {
	private String title;
	private String client_name;
	private String description;
	private String initial_suspect;
	private String status;
	private String date_Time;

	public Stock_case() {

	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getClient_name() {
		return client_name;
	}

	public void setClient_name(String client_name) {
		this.client_name = client_name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getInitial_suspect() {
		return initial_suspect;
	}

	public void setInitial_suspect(String initial_suspect) {
		this.initial_suspect = initial_suspect;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDate_Time() {
		return date_Time;
	}

	public void setDate_Time(String date_Time) {
		this.date_Time = date_Time;
	}

}
